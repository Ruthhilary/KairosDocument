function injectIcons() {
  if (typeof document === 'undefined') return

  const links = document.querySelectorAll('.VPSidebarItem.level-1 .VPLink .text')
  links.forEach((el) => {
    const text = el.textContent?.trim() ?? ''
    const d = iconMap[text]
    if (!d || el.previousElementSibling?.classList.contains('sidebar-icon')) return

    const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg')
    svg.setAttribute('viewBox', '0 0 24 24')
    svg.setAttribute('fill', 'none')
    svg.setAttribute('stroke', 'currentColor')
    svg.setAttribute('stroke-width', '1.5')
    svg.setAttribute('stroke-linecap', 'round')
    svg.setAttribute('stroke-linejoin', 'round')
    svg.setAttribute('aria-hidden', 'true')
    svg.classList.add('sidebar-icon')

    // Handle multi-path icons (separated by M after first)
    const paths = d.split(' M ').map((p, i) => (i > 0 ? 'M ' + p : p))
    paths.forEach((pathD) => {
      const path = document.createElementNS('http://www.w3.org/2000/svg', 'path')
      path.setAttribute('d', pathD)
      svg.appendChild(path)
    })

    el.parentNode?.insertBefore(svg, el)
  })
}

let observer: MutationObserver | null = null

onMounted(() => {
  injectIcons()
  // Re-run when sidebar changes (navigation)
  observer = new MutationObserver(injectIcons)
  const sidebar = document.querySelector('.VPSidebar')
  if (sidebar) observer.observe(sidebar, { childList: true, subtree: true })
})

onUnmounted(() => {
  observer?.disconnect()
})
